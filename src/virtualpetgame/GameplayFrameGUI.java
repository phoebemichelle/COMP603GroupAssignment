    private void feedButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_feedButtonActionPerformed
        // TODO add your handling code here:
        if (pet.getHunger() > 60) {
            showMessage("Mmh, it seems like " + pet.getName() +
                    " is too full to eat right now. Try again later.");
        } else {
            pet.feed();
            showMessage(pet.getName() + " is now full!");
            updateProgressBars();
        }
    }//GEN-LAST:event_feedButtonActionPerformed

    private void waterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_waterButtonActionPerformed
        // TODO add your handling code here:
        if (pet.getThirst() > 60) {
            showMessage("Your pet is not thirsty. No need for water right now.");
        } else {
            pet.fillWaterBowl();
            showMessage(pet.getName() + " feels refreshed!");
            updateProgressBars();
        }
    }//GEN-LAST:event_waterButtonActionPerformed

    private void playButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_playButtonActionPerformed
        // TODO add your handling code here:
        if (pet.getEnergy() < 20) {
            showMessage("Oh no! " + pet.getName() + " is too tired to play right now. Try "
                    + "letting " + pet.getName() + " get some rest first.");
        } else if (pet.getHunger() < 20) {
            showMessage("Oh no! " + pet.getName() + " is too hungry to play right now. Try "
                    + "feeding " + pet.getName() + " first.");
        } else if (pet.getBladder() < 20) {
            showMessage(pet.getName() + " needs to go to the toilet first before playing!");
        } else {
            pet.play();
            showMessage(pet.getName() + " had so much fun!");
            updateProgressBars();
        }
    }//GEN-LAST:event_playButtonActionPerformed

    private void walkButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_walkButtonActionPerformed
        // TODO add your handling code here:
        if (pet.getEnergy() < 30) {
            showMessage("Oh no! " + pet.getName() + " is too tired to go for a walk right now. Try "
                    + "letting " + pet.getName() + " get some rest first.");
        } else if (pet.getHunger() < 20) {
            showMessage("Oh no! " + pet.getName() + " is too hungry right now. Try "
                    + "feeding " + pet.getName() + " first.");
        } else if (pet.getBladder() < 20) {
            showMessage(pet.getName() + " needs to go potty first!");
        } else {
            pet.walk();
            showMessage(pet.getName() + " likes going on walks with you!");
            updateProgressBars();
        }
    }//GEN-LAST:event_walkButtonActionPerformed

    private void sleepButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sleepButtonActionPerformed
        // TODO add your handling code here:
        if (pet.getEnergy() > 40) {
            showMessage("Hmm, it seems like " + pet.getName() + " is not tired.");
        } else if (pet.getHunger() < 20) {
            showMessage("Oh no! " + pet.getName() + " is too hungry right now. Try "
                    + "feeding " + pet.getName() + " first.");
        } else if (pet.getBladder() < 20) {
            showMessage(pet.getName() + " needs to go potty first!");
        } else {
            pet.sleep();
            showMessage("ZZZZZZ... " + pet.getName() + " is sound asleep!");
            updateProgressBars();
        }
    }//GEN-LAST:event_sleepButtonActionPerformed

