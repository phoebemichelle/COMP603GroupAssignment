    //Method to set the pet image based on the type of animal it is
    private void setPetImage() {
        String imagePath;
        if(pet instanceof Cat) {
            imagePath = "/resources/cat.png";
        } else {
            imagePath = "/resources/dog.png";
        }
        //Set the icon of the pet image label
        petImageLabel.setIcon(new ImageIcon(imagePath));
    }

    // Method to update progress bars based on pet's current state
